// Month names by language
const MONTHS_DA = ['jan', 'feb', 'mar', 'apr', 'maj', 'jun', 'jul', 'aug', 'sep', 'okt', 'nov', 'dec'];
const MONTHS_EN = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

export function getLang(): Lang {
  return (localStorage.getItem('synergyhub_lang') as Lang) || 'da';
}

export function setLang(lang: Lang): void {
  localStorage.setItem('synergyhub_lang', lang);
}

export function t(key: string): string {
  const entry = T[key];
  if (!entry) return key;
  return entry[getLang()] || entry['da'] || key;
}

export function getMonths(): string[] {
  return getLang() === 'en' ? MONTHS_EN : MONTHS_DA;
}

export function getLocale(): string {
  return getLang() === 'en' ? 'en-GB' : 'da-DK';
}
